// The sum of odd numbers

let num = 81;
let sum = 0;

while(num <= 131){
    console.log('Odd number is:', num);
    sum = sum + num;
    console.log('The sum of odd number is:', sum);
    num+=2;
}

// The sum of even numbers

let n = 206;
let sum2 = 0;

while(n < 311){
    console.log('Even number is:', n);
    sum2 = sum2 + n;
    console.log('The sum of even number is:', sum2);
    n+=2;
}