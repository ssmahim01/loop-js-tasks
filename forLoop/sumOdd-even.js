// Sum of Odd numbers

let sum = 0;

for(let n = 91; n <= 129; n += 2){
    console.log('Odd number is:', n);
    sum = sum + n;
    console.log('The sum of odd number is:', sum);
}

// Sum of Even Numbers

let sum2 = 0;

for(let num = 52; num < 85; num += 2){
    console.log('Even number is:', num);
    sum2 = sum2 + num;
    console.log('The sum of Even number is:', sum2);
}

